def area(base_length, height):
    """
    Function to calculate the area of a parallelogram given its base length and height.
    
    Parameters:
    base_length (float or int): Length of the base of the parallelogram.
    height (float or int): Height of the parallelogram (perpendicular distance between the base and the opposite side).
    
    Returns:
    float: Area of the parallelogram.
    """
    return base_length * height

