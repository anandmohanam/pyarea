def area(side_length):
    """
    Function to calculate the surface area of a cube given the length of its side.
    
    Parameters:
    side_length (float or int): Length of the side of the cube.
    
    Returns:
    float: Surface area of the cube.
    """
    return 6 * side_length ** 2

